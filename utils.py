def format_prompt_with_template(question):
    required_ending = "Let's think step by step and output the final answer within \\boxed{} "

    if question.endswith(required_ending):
        question = question.replace(required_ending, "")

    SYSTEM_PROMPT = f"""You are given a problem. Think about the problem and \
    provide your reasoning. Place it between {reasoning_start} and \
    {reasoning_end}. Then, provide the final answer (i.e., just one numerical \
    value) between {solution_start} and {solution_end}."""

    TEMPLATE = """user
    {system_prompt}

    {question}
    model"""
    return TEMPLATE.format(
                  system_prompt=SYSTEM_PROMPT,
                  question=question,
              )

def reward_function_gsm8k(prompts, completions, answer, **kargs):
    match_format = re.compile(
    rf"^[\s]{{0,}}"
    rf"{reasoning_start}.+?{reasoning_end}.*?"
    rf"{solution_start}(.+?){solution_end}"
    rf"[\s]{{0,}}$",
    flags=re.MULTILINE | re.DOTALL,
    )

    match_format.search(
        f"{reasoning_start}Let me"
        f" think!{reasoning_end}{solution_start}2{solution_end}",
    )

    responses = completions

    extracted_responses = [
        guess.group(1) if (guess := match_format.search(r)) is not None else None
        for r in responses
    ]
    scores = []
    for guess, true_answer in zip(extracted_responses, answer):
        score = 0
        if guess is None:
            scores.append(0)
            continue
        # Correct answer gets 3 points!
        if guess == true_answer:
            score += 3.0
        # Match if spaces are seen
        elif guess.strip() == true_answer.strip():
            score += 1.5
        else:
            # We also reward it if the answer is close via ratios!
            # Ie if the answer is within some range, reward it!
            try:
                ratio = float(guess) / float(true_answer)
                if ratio >= 0.9 and ratio <= 1.1:
                    score += 0.5
                elif ratio >= 0.8 and ratio <= 1.2:
                    score += 0.25
                else:
                    score -= 1.0  # Penalize wrong answers
            except:
                score -= 0.5  # Penalize
        scores.append(score)
    return scores


